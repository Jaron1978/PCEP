# Operators are special symbools in Python used to create mathmatical operations. 

# Addition
print(2 + 3)

# Subtraction
print(4 - 3)

# Multiplication
print(3 * 3)

# There are three different types of division: 
# Standard Division (Float)
print(4 / 2)

# Integer Division (Nearest whole numberer rounded.)
print(4 // 2)

# Modulus Operation (Returns the remainder)
print(6 % 2) # 6 by 2 has a return of 0, 7 % 2 will be 1 as there is 1 left over. 

# Power Operator
print(2 ** 3) # 2 X 2 = 4 X 2 = 8

# Integer + Integer = Integer
# Integer + Float = Float